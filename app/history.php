    <p>У лютому 2017 року група активних студентів з України поїхала на Міжнародну молодіжну конференцію, яка відбулася у Таїланді (Бангкок). У конференції взяло участь близько 4 000 студентів з усього світу. У рамках конференції також був проведений Всесвітній форум лідерів освіти та Форум молодіжних лідерів. На цьому заході українські студенти представили українську культуру та виступили з доповіддю про діяльність неурядових організацій в Україні. Також, відвідавши Форум молодіжних лідерів, дізналися про проблеми, що їх має іноземна молодь, та шляхи вирішення цих проблем.</p>
    <p><img src="img/history/hands.jpg" class="img-responsive img-left" alt=""> Повернувшись додому, учасники конференції організували в Україні Відкритий форум молодіжних лідерів у березні 2017 року у місті Києві, у якому взяли участь лідери молодіжних, студентських та волонтерських організацій України. Також спеціальними гостями виступили іноземні представники неурядових організацій та навчальних закладів Японії, Грузії, Китаю, Молдови, Кореї та Тайланду. Під час Форуму обговорювалися проблеми сучасної молоді та способи їх вирішення. Лідери організацій виступили з доповідями, розповівши про свою діяльність, проведені заходи, поставлені цілі та досягнення, запропонували різноманітні шляхи досягнення поставлених цілей та вирішили об’єднати зусилля організацій, що працюють у різних сферах.
</p>
<p>З метою об’єднати зусилля молодих та ініціативних людей, що стануть лідерами майбутнього та виведуть нашу країну на новий рівень, після Форуму було прийнято рішення заснувати Всеукраїнську молодіжну організацію під назвою Global Youth Community. У самій назві криється її ідея – молодь, що змінить суспільство. Через розвиток ІТ технологій та сфери електроніки сучасна молодь стає більш замкненою та має менше живого спілкування з людьми. Якщо виникають особисті проблеми, то юнаки та дівчата не мають кому розповісти про них чи попросити допомоги. Молоді люди знаходяться в обмеженнях своєї свідомості, через що, зустрічаючи труднощі на життєвому шляху, впадають у депресію чи залежність, що може призвести навіть до самогубства. 
</p>
<p><img src="img/history/nbnm.jpg" class="img-responsive img-right" alt="">Через різноманітні освітні та культурні програми ми надаємо можливість кожному молодому українцю/українці познайомитися з культурами інших країн, поспілкуватися зі студентами та молоддю, що мають інші інтереси та погляди. Об’єднавшись з активною молоддю університетів, ми проведемо ряд освітніх заходів, відправимо багато студентів у інші країни у якості волонтерів для того, щоб вони змогли побачити інший світ та глибше пізнати самих себе. Дамо змогу побачити проблеми, з якими зустрічається молодь інших країн, змінити свій погляд на речі та розширити світогляд. Подивитись на розвиненість інших держав та навчитись як досягти економічного розвитку у своїй країні, допомагати людям в скрутному становищі та навчитись щирості та вдячності.</p>
<p>Відтепер Global Youth буде запрошувати відомих людей, які досягли успіху у своєму житті та лекторів, які зможуть поділитися своїм досвідом, організовувати освітні табори та конференції для студентів, на яких студенти самі зможуть спостерігати за напрямком руху суспільства країни та обговорювати зміни, які можуть допомогти у розвитку. 
</p>
<p>Для того, щоб людина змінилася та, надалі, вплинула на зміни у країні, необхідно надати можливість подивитися на світ під іншим кутом зору через спілкування з представниками інших країн, видатними людьми, що досягли успіху у своєму житті та навчились не уникати проблем, а долати життєві труднощі та негаразди.
</p>
<p>У наш час молодь України прагне змінити державу на краще, але зміна країни починається з самого себе.. Тому, об’єднавши зусилля ми створили платформу для того, щоб молодь України змогла реалізувати себе у всіх напрямках особистого та громадського простору.
</p>
